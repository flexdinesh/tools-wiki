import { readdir, writeFile } from 'node:fs/promises';
import { join } from 'node:path';

const DIST = 'dist';

async function* walk(dir) {
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const p = join(dir, entry.name);
    if (entry.isDirectory()) yield* walk(p);
    else if (entry.name.endsWith('.md')) yield p;
  }
}

const paths = [];
for await (const p of walk(DIST)) {
  paths.push('/' + p.replace(/^dist\//, '').replace(/\.md$/, ''));
}

await writeFile(
  'src/md-paths.ts',
  `// Auto-generated by scripts/generate-md-manifest.mjs — DO NOT EDIT
export const MD_PATHS = new Set(${JSON.stringify(paths)});
`
);

console.log(`Generated md-paths.ts with ${paths.length} paths:`, paths);